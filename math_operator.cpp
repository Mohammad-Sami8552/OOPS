#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "Enter the two numbers:\n";
    cin >> a >> b;
    cout << "The addition of the two numbers = " << a + b << endl;
    cout << "The subtraction of the two numbers = " << a - b << endl;
    cout << "The multiplication of the two numbers = " << a * b << endl;
    cout << "The division of the two numbers = " << a / b << endl;
    return 0;
}